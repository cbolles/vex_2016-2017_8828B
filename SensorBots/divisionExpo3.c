#pragma config(Sensor, in2,    leftPot,        sensorPotentiometer)
#pragma config(Motor,  port1,           right,         tmotorVex393_HBridge, openLoop)
#pragma config(Motor,  port2,           left,          tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port3,           rightArm1,     tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           rightArm2,     tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           leftArm1,      tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port6,           leftArm2,      tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port7,           rightClaw,     tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port8,           leftClaw,      tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port9,           left2,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port10,          right2,        tmotorVex393_HBridge, openLoop, reversed)
